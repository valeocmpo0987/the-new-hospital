$nombreUsuario = $_SESSION['usuario'];
// Consulta SQL para obtener el campo "nombres" de la base de datos
$sql = "SELECT nombres FROM usuarios WHERE usuario = :usuario";
$stmt = $conn->prepare($sql);
$stmt->bindParam(':usuario', $nombreUsuario);
$stmt->execute();
$resultado = $stmt->fetch(PDO::FETCH_ASSOC);

